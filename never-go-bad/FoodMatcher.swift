import Foundation

struct FoodDesc: Hashable {
    var name:String
    var photoUrl:String
    
    var hashValue:Int {
        return name.hashValue
    }
}

func ==(lhs: FoodDesc, rhs: FoodDesc) -> Bool {
    return lhs.name == rhs.name
}

func matchFood(description: String) -> Set<FoodDesc> {
    var results = Set<FoodDesc>()
    
    for candidate in description.words() {
        for  i in 0..<foodDictionary.count {
            if candidate.length > 3 && foodDictionary[i].contains(candidate) {//Filter pronouns and conjunctions
                results.insert(FoodDesc(name: foodDictionary[i], photoUrl: foodPhotos[i]))
            }
        }
    }
    
    return results
}

